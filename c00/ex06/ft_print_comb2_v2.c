#include <unistd.h>

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = '0';
	while (a < '9')
	{
		b = '0';
		while (b <= '9')
		{
			if (a != b)
			{
				write(1, &a, 2);
				write(1, ' ', 1);
				write(1, &b, 2);
				if (!(a == '9' && b == '9'))
					write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}

}

int	main()
{
	ft_print_comb2();
}
